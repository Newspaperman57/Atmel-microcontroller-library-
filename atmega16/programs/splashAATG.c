const unsigned char nSprites = 26;

void Splash() {
	int i,j;

	while(1) {
		for(i = 0; i < nSprites; i++) {
			//lcd_clear();
			for(j = 0; j < nPositions[i]; j++) {
				lcd_set_char(j,(unsigned char*)sprites[i][j]);
				lcd_gotoxy(positions[i][j][0],positions[i][j][1]);
				lcd_write_data(j);
			}
			_delay_ms(100);
		}
	}

	_delay_ms(1000);
	lcd_clear();


}

